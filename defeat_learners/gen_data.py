def best_4_lin_reg(seed=1489683273):
    rng = np.random.RandomState(seed)
    rows = 500
    cols = 5
    X = rng.uniform(-1.0,1.0,(rows,cols))
    Weights = np.array([1.5,-2.2,3.4,1.3,-1.3],dtype=float)
    Y = X @ Weights
    return X.astype(float), Y.astype(float)
  		  	   		 	 	 		  		  		    	 		 		   		 		  
  		  	   		 	 	 		  		  		    	 		 		   		 		  
def best_4_dt(seed=1489683273):
    rng = np.random.RandomState(seed)
    n, d = 600, 5
    X = rng.uniform(-1, 1, size=(n, d))

    y = (
        (X[:, 0] > 0).astype(float)
        + (X[:, 1] > 0.5).astype(float)
        + (X[:, 1] < -0.5).astype(float)
        + (X[:, 2] > 0).astype(float) * (X[:, 3] < 0).astype(float) * 2.0
    )

    y = y + rng.normal(0, 0.15, size=n)

    return X.astype(float), y.astype(float)

  		  	   		 	 	 		  		  		    	 		 		   		 		  
